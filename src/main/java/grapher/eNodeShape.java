package grapher;

/**
 * Enum describing the shape of a {@link Node}.
 * For UI purposes, use with {@link NodeShapeBuilder}
 */
public enum eNodeShape {
    /**
     * Rectangle.
     */
    RECTANGLE,
    /**
     * Circle.
     */
    CIRCLE,
    /**
     * Right-pointing triangle.
     */
    RIGHT_TRI
}
